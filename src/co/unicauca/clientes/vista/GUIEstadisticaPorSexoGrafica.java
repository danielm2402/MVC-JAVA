    @Override
    public void update(Observable obs, Object obj) {
        ClientesDB cliDB = (ClientesDB) obs;
        this.setTitle("Profesi√≥n: "+ cliDB.getProfesion());
        this.repaint=true;
        this.hombres= cliDB.getTotalHombres();
        this.mujeres=cliDB.getTotalMujeres();
        this.total=cliDB.getClientesPorProfesion().size();
                
        repaint();
        
    }
    public void paint(Graphics g2d){
        g = (Graphics2D) g2d;
        if(this.repaint){
            this.setVisible(false);
          this.setVisible(true);
          g.fillRect(0,0,600,280);
          g.setColor(new Color(28,35,57));
          g.drawRect(300, 50, 60, 250);
          g.drawRect(200, 50, 60, 250);
          float porcentajeM= (this.mujeres*100)/this.total;
          float pixelesM= (porcentajeM*300)/100;
          
          float porcentajeH= (this.hombres*100)/this.total;
          float pixelesH= (porcentajeH*300)/100;
        
          g.fillRect(200, ((Math.round(pixelesH))+50) , 60, 300-((Math.round(pixelesH))+50));
          
          g.fillRect(300, ((Math.round(pixelesM))+50) , 60, 300-((Math.round(pixelesM))+50));
     
        
        
        this.repaint=false;
        } 
        
    }
